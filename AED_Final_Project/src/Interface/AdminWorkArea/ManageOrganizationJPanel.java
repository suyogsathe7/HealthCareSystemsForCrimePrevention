    private void populateOrgTable()
    {
        DefaultTableModel dtm=(DefaultTableModel) tblOrganisation.getModel();
        dtm.setRowCount(0);
        for(Organization o:ordDir.getLstOrganisation())
        {
            Object[] row=new Object[2];
            row[0]=o.getOrganizationID();
            row[1]=o.getName();
            dtm.addRow(row);
        }
    }
    private void populateOrgList()
    {
        cbOrganisationlist.removeAllItems();
        for(OrgType ot:Organization.OrgType.values())
        {
            if(etrp.getEt().equals(Enterprise.ErpType.Hospital))
            {
                if(ot.getValue().equals(OrgType.Doctor.getValue()) || ot.getValue().equals(OrgType.Lab.getValue()))
                {
                     cbOrganisationlist.addItem(ot);
                }
            }
            else if(etrp.getEt().equals(Enterprise.ErpType.Security))
            {
                //#TODO enter for other enterprises.
               if(ot.getValue().equals(OrgType.PoliceDepartment.getValue()))
               {
                    cbOrganisationlist.addItem(ot);
               }
            }
            else if(etrp.getEt().equals(Enterprise.ErpType.Ngo))
            {
                if(ot.getValue().equals(OrgType.SuicideAwarenessFoundation.getValue()))
                {
                    cbOrganisationlist.addItem(ot);
                }
                
            }
        }
    }
